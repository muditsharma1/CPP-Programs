#include <iostream>
using namespace std;
int main()
{
    //arithmatic operator
    int a = 5, b = 4;
    cout << "a+b=" << a + b << endl;
    cout << "a-b=" << a - b << endl;
    cout << "a*b=" << a * b << endl;
    cout << "a/b=" << a / b << endl;
    cout << "a%b=" << a % b << endl;
    cout<<endl;
    //comparison operator
    cout << "a==b=" << (a == b) << endl;
    cout << "a!=b=" << (a != b) << endl;
    cout << "a>=b=" << (a >= b) << endl;
    cout << "a<=b=" << (a <= b) << endl;
    cout << "a<b=" << (a < b) << endl;
    cout << "a>b=" << (a > b) << endl;
    return 0;
}